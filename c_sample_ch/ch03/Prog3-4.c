#include <stdio.h>
#include <stdlib.h>
int main(void) 
{
	double dVal = 9453.59;  
	printf("123456789012345\n"); //陪ボw欹
	printf("%f\n",dVal); //场块X,p计IUTw6
	printf("%e\n",dVal); //场块X,p计IUTw6
	printf("%4.2f\n",dVal); //4应rじ,p计IU2
	printf("%10.1f\n",dVal); //10应rじ,p计IU1
	printf("%010.3f\n",dVal); //10应rじ,p计IU3,e备0
	printf("%15.4e\n",dVal); //15应rじ,p计IU4
	printf("%+10.2f\n",dVal); //块Xタt腹,10应rじ,p计IU2
	system("pause");
	return(0);
}
